package json_conversion.helper_classes;

public class ProduktyJson {

    public final double jedzenie, ubrania, narzedzia, diamenty, programy;

    public ProduktyJson(double jedzenie, double ubrania, double narzedzia, double diamenty, double programy) {
        this.jedzenie = jedzenie;
        this.ubrania = ubrania;
        this.narzedzia = narzedzia;
        this.diamenty = diamenty;
        this.programy = programy;
    }
}
