package json_conversion.helper_classes;

public class CenyJson {

    public final double programy;
    public final double jedzenie;
    public final double ubrania;
    public final double narzedzia;

    public CenyJson(double jedzenie, double ubrania, double narzedzia, double programy) {
        this.programy = programy;
        this.jedzenie = jedzenie;
        this.ubrania = ubrania;
        this.narzedzia = narzedzia;
    }

}
